/**
 * Operadores de comparação
 * > maior que
 * < menor que 
 * >= maior ou igual a
 * <= menor ou igual a
 * == igualdade - valor *
 * === igualdade estrita - valor e tipo
 * != diferente - valor *
 * !== diferente estrito - valor e tipo
 * (*) não recomendado para uso
 */
const numeroUm = 10;
const numeroDois = 11;

const comp = numeroUm > numeroDois;
console.log(comp);

const comp1 = numeroUm < numeroDois;
console.log(comp1);

const comp2 = numeroUm >= numeroDois;
console.log(comp2);

const comp3 = numeroUm <= numeroDois;
console.log(comp3);

const comp4 = numeroUm === numeroDois;
console.log(comp4);

const comp5 = numeroUm !== numeroDois;
console.log(comp5);
